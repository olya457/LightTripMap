  lightningSectors.find(sector => sector.id === id);

export const getLightningSite = (id: string) =>
  lightningSites.find(site => site.id === id);

export const getSitesBySector = (sectorId: string) =>
  lightningSites.filter(site => site.sectorId === sectorId);
